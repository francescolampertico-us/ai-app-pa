"""
Report Generator
=================
Generates markdown reports for search results, bill summaries, and watchlist status.
"""

import json
from datetime import datetime
from pathlib import Path


class ReportGenerator:
    """Generates markdown reports for the Legislative Tracker."""

    @staticmethod
    def search_report(query: str, state: str, year: int, results: list[dict]) -> str:
        """Generate a markdown report of search results."""
        lines = [
            f"# Legislative Search Report",
            f"",
            f"**Query:** {query}",
            f"**Jurisdiction:** {state}",
            f"**Year:** {year or 'Current session'}",
            f"**Results:** {len(results)} bills found",
            f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M')}",
            f"",
            f"---",
            f"",
        ]

        if not results:
            lines.append("No bills found matching the query.")
            return "\n".join(lines)

        lines.append("| # | Bill | State | Status | Last Action | Date |")
        lines.append("|---|------|-------|--------|-------------|------|")

        for i, bill in enumerate(results, 1):
            number = bill.get("number", "N/A")
            title = bill.get("title", "")
            if len(title) > 60:
                title = title[:57] + "..."
            state_code = bill.get("state", "")
            status = bill.get("status", "")
            last_action = bill.get("last_action", "")
            if len(last_action) > 40:
                last_action = last_action[:37] + "..."
            date = bill.get("last_action_date", "")
            url = bill.get("url", "")

            bill_link = f"[{number}]({url})" if url else number
            lines.append(f"| {i} | {bill_link} — {title} | {state_code} | {status} | {last_action} | {date} |")

        lines.append("")
        lines.append("---")
        lines.append("")
        lines.append("*Report generated by Legislative Tracker (AI-Powered PA Toolkit). "
                      "Bill status may have changed since generation. Verify before external use.*")

        return "\n".join(lines)

    @staticmethod
    def bill_summary_report(bill_detail: dict, bill_header: str, ai_summary: str) -> str:
        """Combine bill header and AI summary into a full report."""
        lines = [
            f"# Bill Summary: {bill_detail.get('number', 'N/A')} — {bill_detail.get('title', 'N/A')}",
            f"",
            f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M')}",
            f"**Risk Level:** Yellow — requires human review before distribution",
            f"",
            f"---",
            f"",
            bill_header,
            f"",
            f"---",
            f"",
            ai_summary,
            f"",
            f"---",
            f"",
            f"## Human Review Checklist",
            f"",
            f"- [ ] Bill number and title match official sources",
            f"- [ ] AI summary accurately reflects bill provisions",
            f"- [ ] Talking points are balanced (for AND against)",
            f"- [ ] Sponsor names and party affiliations are correct",
            f"- [ ] Status information is current",
            f"- [ ] Assumptions & Unknowns section reviewed",
            f"",
            f"*Report generated by Legislative Tracker (AI-Powered PA Toolkit). "
            f"AI-generated content requires human review before external distribution.*",
        ]
        return "\n".join(lines)

    @staticmethod
    def watchlist_report(bills: list[dict], refresh_results: list[dict] = None) -> str:
        """Generate a watchlist status report."""
        lines = [
            f"# Legislative Watchlist",
            f"",
            f"**Bills tracked:** {len(bills)}",
            f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M')}",
            f"",
            f"---",
            f"",
        ]

        if not bills:
            lines.append("No bills in watchlist. Use `--watchlist add --bill-id <ID>` to add bills.")
            return "\n".join(lines)

        # If we have refresh results, highlight changes
        if refresh_results:
            changed = [r for r in refresh_results if r.get("changed")]
            if changed:
                lines.append(f"### Status Changes Detected ({len(changed)})")
                lines.append("")
                for r in changed:
                    lines.append(f"- **{r['number']}** ({r['state']}): "
                                 f"{r.get('old_status', '?')} → **{r.get('new_status', '?')}**")
                    if r.get("new_action") != r.get("old_action"):
                        lines.append(f"  - New action: {r.get('new_action', '')}")
                lines.append("")
                lines.append("---")
                lines.append("")
            else:
                lines.append("*No status changes since last check.*")
                lines.append("")

        lines.append("| Bill | State | Status | Last Action | Date | Tracked Since |")
        lines.append("|------|-------|--------|-------------|------|---------------|")

        for bill in bills:
            number = bill.get("number", "N/A")
            url = bill.get("url", "")
            bill_link = f"[{number}]({url})" if url else number
            state = bill.get("state", "")
            status = bill.get("status", "")
            last_action = bill.get("last_action", "")
            if len(last_action) > 40:
                last_action = last_action[:37] + "..."
            date = bill.get("last_action_date", "")
            added = bill.get("added_at", "")[:10]

            lines.append(f"| {bill_link} | {state} | {status} | {last_action} | {date} | {added} |")

        return "\n".join(lines)
