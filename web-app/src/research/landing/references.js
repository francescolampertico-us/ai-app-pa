export function citationTitle(refs = []) {
  return refs
    .map((ref) => {
      const entry = REFERENCES[ref];
      if (!entry) return ref;
      return entry.url ? `${entry.apa}\n${entry.url}` : entry.apa;
    })
    .join('\n\n');
}
